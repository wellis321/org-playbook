                <p>
                    And at the human level — which is the only level that ultimately matters — what this means is that people get their information back. Staff who have been locked out of systems sold to their organisation by vendors who were never really serving them. People who receive services and have no access to their own data, who exist in someone else's system as a record rather than as a participant. The shift that is possible now, with AI in everyone's hands and the barrier to building collapsing, is to invert that relationship entirely. The individual carries their own data. They grant access to the services they choose. Authentication and identity become the critical layer — not as gatekeeping, but as genuine sovereignty.
                </p>

                <p>
                    The agile manifesto was reaching for something like this. Not sprints and standups and velocity points — those are just waterfall in shorter cycles. It was asking something harder. Stay close to real people. Stay close to real needs. Resist the gravity of process for its own sake. Keep asking: does this actually serve a human being?
                </p>

                <p>
                    JSON in a folder is a small, honest answer to that question. It is also, if you follow it seriously, the beginning of an organisation that can finally think.
                </p>
